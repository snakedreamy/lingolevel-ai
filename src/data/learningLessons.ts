import { LEARNING_CONCEPTS } from './learningCurriculum'

export type MasteryDimension = 'recognition' | 'understanding' | 'construction' | 'application'

interface LessonBaseActivity {
  id: string
  dimension: MasteryDimension
  prompt: string
  explanation: string
}

export interface ChoiceActivity extends LessonBaseActivity {
  type: 'choice'
  options: string[]
  answer: number
}

export interface OrderActivity extends LessonBaseActivity {
  type: 'order'
  tokens: string[]
  answer: string[]
}

export interface InputActivity extends LessonBaseActivity {
  type: 'input'
  answers: string[]
  hint: string
}

export interface ApplyActivity extends LessonBaseActivity {
  type: 'apply'
  checklist: string[]
  modelAnswer: string
}

export type LearningActivity = ChoiceActivity | OrderActivity | InputActivity | ApplyActivity

export interface LearningLesson {
  id: string
  conceptId: string
  title: string
  objective: string
  estimatedMinutes: number
  situation: string
  examples: Array<{ english: string; chinese: string; parts: string[] }>
  observation: string
  explanation: Array<{ title: string; body: string }>
  rules: string[]
  contrasts: Array<{ wrong: string; right: string; reason: string }>
  activities: LearningActivity[]
  summary: string[]
  reviewPrompt: string
}

const choice = (id: string, dimension: MasteryDimension, prompt: string, options: string[], answer: number, explanation: string): ChoiceActivity =>
  ({ id, type: 'choice', dimension, prompt, options, answer, explanation })
const order = (id: string, prompt: string, tokens: string[], answer: string[], explanation: string): OrderActivity =>
  ({ id, type: 'order', dimension: 'construction', prompt, tokens, answer, explanation })
const input = (id: string, prompt: string, answers: string[], hint: string, explanation: string): InputActivity =>
  ({ id, type: 'input', dimension: 'construction', prompt, answers, hint, explanation })
const apply = (id: string, prompt: string, checklist: string[], modelAnswer: string, explanation: string): ApplyActivity =>
  ({ id, type: 'apply', dimension: 'application', prompt, checklist, modelAnswer, explanation })

function starterLesson(args: {
  id: string; conceptId: string; title: string; objective: string; minutes: number; situation: string
  examples: LearningLesson['examples']; observation: string; concept: string; use: string
  rules: string[]; contrast: LearningLesson['contrasts'][number]; activities: LearningActivity[]
  summary: string[]; reviewPrompt: string
}): LearningLesson {
  return {
    id: args.id, conceptId: args.conceptId, title: args.title, objective: args.objective,
    estimatedMinutes: args.minutes, situation: args.situation, examples: args.examples,
    observation: args.observation,
    explanation: [{ title: '核心概念', body: args.concept }, { title: '实际使用', body: args.use }],
    rules: args.rules, contrasts: [args.contrast], activities: args.activities,
    summary: args.summary, reviewPrompt: args.reviewPrompt,
  }
}

