export const LESSON_BY_ID = new Map(LEARNING_LESSONS.map((lesson) => [lesson.id, lesson]))

export function validateLearningLessons(): void {
  const conceptById = new Map(LEARNING_CONCEPTS.map((concept) => [concept.id, concept]))
  const lessonIds = new Set<string>()
  const activityIds = new Set<string>()
  for (const lesson of LEARNING_LESSONS) {
    if (lessonIds.has(lesson.id)) throw new Error(`Duplicate learning lesson: ${lesson.id}`)
    lessonIds.add(lesson.id)
    const concept = conceptById.get(lesson.conceptId)
    if (!concept) throw new Error(`Unknown concept ${lesson.conceptId} for lesson ${lesson.id}`)
    if (concept.lessonId !== lesson.id) throw new Error(`Lesson link mismatch: ${lesson.id}`)
    for (const prerequisiteId of concept.prerequisites) {
      if (!conceptById.get(prerequisiteId)?.lessonId) {
        throw new Error(`Playable lesson ${lesson.id} has unavailable prerequisite ${prerequisiteId}`)
      }
    }
    if (!lesson.objective || !lesson.situation || lesson.examples.length < 2 || lesson.explanation.length < 2
      || lesson.rules.length < 2 || lesson.contrasts.length < 1 || lesson.summary.length < 2 || !lesson.reviewPrompt) {
      throw new Error(`Incomplete teaching structure: ${lesson.id}`)
    }
    const dimensions = new Set(lesson.activities.map((activity) => activity.dimension))
    for (const dimension of ['recognition', 'understanding', 'construction', 'application'] as const) {
      if (!dimensions.has(dimension)) throw new Error(`Lesson ${lesson.id} misses ${dimension}`)
    }
    for (const activity of lesson.activities) {
      if (activityIds.has(activity.id)) throw new Error(`Duplicate learning activity: ${activity.id}`)
      activityIds.add(activity.id)
      if (activity.type === 'choice' && (activity.options.length < 2 || activity.answer < 0 || activity.answer >= activity.options.length)) {
        throw new Error(`Invalid choice activity: ${activity.id}`)
      }
      if (activity.type === 'order' && (activity.tokens.length < 2 || activity.answer.length !== activity.tokens.length)) {
        throw new Error(`Invalid order activity: ${activity.id}`)
      }
      if (activity.type === 'input' && activity.answers.length === 0) throw new Error(`Invalid input activity: ${activity.id}`)
      if (activity.type === 'apply' && (activity.checklist.length < 2 || !activity.modelAnswer)) {
        throw new Error(`Invalid application activity: ${activity.id}`)
      }
    }
    const activityTypes = new Set(lesson.activities.map((activity) => activity.type))
    const hasConstructionTask = activityTypes.has('order') || activityTypes.has('input')
    if (['sentence', 'grammar', 'discourse'].includes(concept.domain) && !hasConstructionTask) {
      throw new Error(`Lesson ${lesson.id} needs a construction task for ${concept.domain}`)
    }
    if (concept.domain === 'communication' && !activityTypes.has('order')) {
      throw new Error(`Communication lesson ${lesson.id} needs an interaction-sequencing task`)
    }
    if (['vocabulary', 'pronunciation'].includes(concept.domain) && !activityTypes.has('input')) {
      throw new Error(`${concept.domain} lesson ${lesson.id} needs a controlled-production task`)
    }
  }
  for (const concept of LEARNING_CONCEPTS.filter((item) => item.lessonId)) {
    if (!lessonIds.has(concept.lessonId ?? '')) throw new Error(`Missing lesson ${concept.lessonId}`)
  }
}

validateLearningLessons()
